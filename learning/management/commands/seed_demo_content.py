class Command(BaseCommand):
    help = 'Creates demo users and fills the course with modules, lessons, practice tasks and tests.'

    def handle(self, *args, **options):
        admin_user, _ = User.objects.update_or_create(
            username=ADMIN_USERNAME,
            defaults={
                'is_staff': True,
                'is_superuser': True,
                'email': 'admin@example.com',
                'first_name': 'Администратор',
            },
        )
        admin_user.set_password(ADMIN_PASSWORD)
        admin_user.save()

        student_user, _ = User.objects.update_or_create(
            username=STUDENT_USERNAME,
            defaults={
                'email': 'student@example.com',
                'first_name': 'Демо',
                'last_name': 'Студент',
            },
        )
        student_user.set_password(STUDENT_PASSWORD)
        student_user.save()

        for module_order, module_data in enumerate(COURSE_DATA, start=1):
            module, _ = Module.objects.update_or_create(
                title=module_data['title'],
                defaults={
                    'description': module_data['description'],
                    'order': module_order,
                    'is_published': True,
                },
            )

            for lesson_order, lesson_data in enumerate(module_data['lessons'], start=1):
                lesson, _ = Lesson.objects.update_or_create(
                    module=module,
                    title=lesson_data['title'],
                    defaults={
                        'goal': lesson_data['goal'],
                        'content': lesson_data['content'],
                        'order': lesson_order,
                        'is_published': True,
                    },
                )
                self.create_practice_task(lesson, lesson_data['practice'], lesson_order)
                self.create_test(lesson, lesson_data['test'], lesson_order)

        self.stdout.write(self.style.SUCCESS('Demo content was created or updated.'))
        self.stdout.write(f'Admin: {ADMIN_USERNAME} / {ADMIN_PASSWORD}')
        self.stdout.write(f'Student: {STUDENT_USERNAME} / {STUDENT_PASSWORD}')

    def create_practice_task(self, lesson, practice_data, order):
        PracticeTask.objects.update_or_create(
            lesson=lesson,
            title=practice_data['title'],
            defaults={
                'objective': practice_data['objective'],
                'assignment': practice_data['assignment'],
                'order': order,
            },
        )

    def create_test(self, lesson, test_data, order):
        knowledge_test, _ = KnowledgeTest.objects.update_or_create(
            lesson=lesson,
            title=test_data['title'],
            defaults={
                'description': test_data['description'],
                'passing_percent': 60,
                'order': order,
                'is_published': True,
            },
        )

        for question_order, (question_text, answers) in enumerate(test_data['questions'], start=1):
            question, _ = Question.objects.update_or_create(
                test=knowledge_test,
                text=question_text,
                defaults={'order': question_order},
            )

            for answer_order, (answer_text, is_correct) in enumerate(answers, start=1):
                Answer.objects.update_or_create(
                    question=question,
                    text=answer_text,
                    defaults={
                        'is_correct': is_correct,
                        'order': answer_order,
                    },
                )
